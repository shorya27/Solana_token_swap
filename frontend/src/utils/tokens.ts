export function findTokenBySymbol(symbol: string): TokenInfo | undefined {
  return SAMPLE_TOKENS.find(token => token.symbol === symbol);
}

export function findTokenByAddress(address: string): TokenInfo | undefined {
  return SAMPLE_TOKENS.find(token => token.address === address);
}

export function formatTokenAmount(amount: number, decimals: number): string {
  return (amount / 10 ** decimals).toLocaleString('en-US', {
    maximumFractionDigits: decimals,
  });
}

export function parseTokenAmount(amount: string, decimals: number): number {
  return parseFloat(amount) * 10 ** decimals;
}